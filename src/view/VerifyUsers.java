    public void getAllRecords(String email,String soDienThoai){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<User> list = UserDAO.getInstance().getAllRecords(email,soDienThoai);
        Iterator<User> itr = list.iterator();
        while(itr.hasNext()){
            User userObject = itr.next();
            if(!userObject.getEmail().equals("admin@gmail.com")){
                dtm.addRow(new Object[]{
                    userObject.getId(),userObject.getTen(),userObject.getEmail(),userObject.getSoDienThoai(),userObject.getDiaChi(),userObject.getCauHoiBaoMat(),userObject.getTinhTrang()
                });
            }
        }
    }
