    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String email = model.getValueAt(index, 2).toString();
        String tinhTrang = model.getValueAt(index, 6).toString();
        JLabel label = new JLabel("Bạn Có Muốn Thay Đổi Trạng Thái Của Người Dùng Này ?");
        label.setFont(new Font("Arial", Font.BOLD, 14));
        int a = JOptionPane.showConfirmDialog(null,label ,"SELECT",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(a==0){
            if(tinhTrang.equals("true")){
                tinhTrang = "false";
            }
            else{
                tinhTrang = "true";
            }
            UserDAO.getInstance().thayDoiTinhTrang(email, tinhTrang);
            setVisible(false);
            new VerifyUsers().setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

