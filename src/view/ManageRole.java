    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Role> list2 = RoleDAO.getInstance().selectAll();
        Iterator<Role> itr2 = list2.iterator();
        while(itr2.hasNext()){
            Role role = itr2.next();
            dtm.addRow(new Object[]{
                role.getId(),role.getTenChucVu(),role.getLuong(),role.getCongViec()
            });
        }
    }//GEN-LAST:event_formComponentShown

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        Role role = new Role();
        int id = Integer.parseInt(lbID.getText());
        role.setId(id);
        role.setTenChucVu(txtTenChucVu.getText());
        role.setLuong(txtLuong.getText());
        role.setCongViec(txtCongViec.getText());
        int kq = RoleDAO.getInstance().insert(role);
        setVisible(false);
        new ManageRole().setVisible(true);
        if(kq > 0){
            JLabel label = new JLabel("Đã Lưu Chức Vụ !");
                label.setFont(new Font("Arial", Font.BOLD, 14));
            JOptionPane.showMessageDialog(null,label,"MESSAGE",JOptionPane.WARNING_MESSAGE );
        }
    }//GEN-LAST:event_btnLuuActionPerformed

