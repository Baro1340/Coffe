    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int id = Integer.parseInt(lbID.getText());
        JLabel label = new JLabel("Bạn Thực Sự Muốn Xóa Chức Vụ Này ?");
        label.setFont(new Font("Arial", Font.BOLD, 14));
        int a = JOptionPane.showConfirmDialog(null, label,"SELECT",JOptionPane.YES_NO_OPTION,JOptionPane.WARNING_MESSAGE);
        if(a==0){
            int kq = RoleDAO.getInstance().delete(id);
            if(kq>0){
                JLabel label2 = new JLabel("Xóa Chức Vụ Thành Công!");
                label.setFont(new Font("Arial", Font.BOLD, 14));
                JOptionPane.showMessageDialog(null,label2,"MESSAGE",JOptionPane.WARNING_MESSAGE );
            }
            setVisible(false);
            new ManageRole().setVisible(true);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

